string = "Coding For All"

# Using capitalize() method
capitalized_string = string.capitalize()
print("Capitalized:", capitalized_string)

# Using title() method
title_case_string = string.title()
print("Title Case:", title_case_string)

# Using swapcase() method
swapped_case_string = string.swapcase()
print("Swapped Case:", swapped_case_string)
